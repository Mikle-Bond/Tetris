inline void TTypeNum (int num)
{

    glBitmap(32, 22, 0.0, 0.0, 33.0, 0.0, FONTNUMBER + (num % 10) * NUMFONTSTEP);
}
//print number
void TPrintNUMBER(int x, int y, int num)
{
    int i, j;
    int len = 0, curr = num;
    int power = 0;
    glColor3f(COLOR[ONESPECIALCOLORFORSCREEN + 1].red / 125.,
              COLOR[ONESPECIALCOLORFORSCREEN + 1].green / 125.,
              COLOR[ONESPECIALCOLORFORSCREEN + 1].blue / 125.);
    glRasterPos2f(-1 + (float)((float)x * TsquareSize) / ( Win_SizeX),
                  1 - (float)((float)y * TsquareSize) / ( Win_SizeY)); //set position

    //glRasterPos2i(x, y);
    while (curr)
    {
        curr/=10;
        len++;
    }
    for (i = 0; i <= len; i++)
    {
        power = 1;
        for (j = 0; j < len - i; j++)
        {
            power*=10;
        }
        TTypeNum(num / power);
    }
}

